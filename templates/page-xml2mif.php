<h2>Программа обработки файлов XML Росреестра</h2>
<p>Программа позволяет сформировать картографическое представление (mif/mid) выданных Росреестром кадастровых сведений в формате XML.</p>
<p>Для работы программа требует наличия в системе интерпретатора Python, а также библиотеки tkinter для графического интерфейса.</p>
